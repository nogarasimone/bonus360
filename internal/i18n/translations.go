// Get returns the translation for the given language and key.
// Falls back to Italian ("it") if the key is not found in the requested language.
// Returns the key itself if not found in any language.
func Get(lang, key string) string {
	if translations, ok := T[lang]; ok {
		if val, ok := translations[key]; ok {
			return val
		}
	}
	// Fallback to Italian
	if translations, ok := T["it"]; ok {
		if val, ok := translations[key]; ok {
			return val
		}
	}
	return key
}

// GetAll returns all translations for the given language.
// Falls back to Italian ("it") if the language is not found.
func GetAll(lang string) map[string]string {
	if translations, ok := T[lang]; ok {
		return translations
	}
	return T["it"]
}

